# ---- Classification ----


def _has_kw(t: str, *keywords: str) -> bool:
    """Word-boundary keyword match — 'see' must not match 'seen'."""
    return any(re.search(rf"\b{re.escape(k)}\b", t) for k in keywords)


_TIME_EXPR = re.compile(
    r"\b(at\s+\d{1,2}([:.]\d{2})?\s*(am|pm)?\b"
    r"|every\s+(day|morning|afternoon|evening|night)"
    r"|daily|each\s+day)\b"
)


def classify(text: str) -> str:
    t = text.lower().strip()

    if t in ("/start", "start", "/cameras", "/setup"):
        return "setup"
    if t.startswith("/reset"):
        return "chat"
    if t.startswith("/scenario") or (
        _has_kw(t, "when", "whenever", "every time", "if")
        and _has_kw(t, "then", "turn", "switch", "alert", "notify", "send")
    ):
        return "scenario"
    # Camera listing — "list/name/what/which/how many cameras"
    if (
        _has_kw(t, "camera", "cameras")
        and _has_kw(t, "list", "name", "names", "which", "what", "all", "how many")
        and not _has_kw(t, "show", "snap", "see", "look", "photo", "picture")
    ):
        return "camera_list"
    if _has_kw(t, "devices", "smart home") or "what devices" in t:
        return "device_list"
    # Weather goes to the HA weather entity (accurate), not camera vision.
    if _has_kw(
        t,
        "weather",
        "raining",
        "rain",
        "forecast",
        "sunny",
        "cloudy",
        "overcast",
        "drizzle",
        "storm",
        "snowing",
        "humid",
        "windy",
    ):
        return "weather"
    if _has_kw(
        t,
        "turn on",
        "turn off",
        "switch on",
        "switch off",
        "lights",
        "light",
        "fan",
        "ac",
        "pump",
        "heater",
        "socket",
        "plug",
        "brightness",
        "dim",
    ):
        # "switch on the heater AT 4AM" is a schedule, not an immediate action
        if _TIME_EXPR.search(t):
            return "scenario"
        return "device"
    if t.startswith("/video") or _has_kw(t, "video", "feed", "live", "stream"):
        return "video"
    if t.startswith("/snap") or _has_kw(
        t, "show", "snap", "see", "look", "photo", "picture", "capture"
    ):
        return "snapshot"
    if (
        t.startswith("/check")
        or "?" in t
        or _has_kw(
            t,
            "is there",
            "is anyone",
            "is the",
            "are there",
            "can you see",
            "do you see",
            "how many",
            "check",
            "detect",
        )
    ):
        return "vision"
    return "chat"


