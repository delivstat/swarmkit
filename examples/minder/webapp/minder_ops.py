def _ha_token() -> str:
    """Get a working HA access token, refreshing if expired."""
    token_file = DATA_DIR / "ha_token.json"
    if not token_file.exists():
        return ""
    try:
        tokens = json.loads(token_file.read_text())
    except (json.JSONDecodeError, ValueError):
        return ""

    access = tokens.get("access_token", "")
    try:
        req = urllib.request.Request(
            f"{HA_URL}/api/", headers={"Authorization": f"Bearer {access}"}
        )
        urllib.request.urlopen(req, timeout=5)
        return access
    except Exception:
        pass

    try:
        refresh = tokens.get("refresh_token", "")
        form = (f"grant_type=refresh_token&refresh_token={refresh}&client_id={HA_URL}/").encode()
        req = urllib.request.Request(
            f"{HA_URL}/auth/token",
            data=form,
            headers={"Content-Type": "application/x-www-form-urlencoded"},
            method="POST",
        )
        new_tokens = json.loads(urllib.request.urlopen(req, timeout=10).read())
        write_json_atomic(
            token_file,
            {
                "access_token": new_tokens["access_token"],
                "refresh_token": refresh,
            },
        )
        return new_tokens["access_token"]
    except Exception:
        return ""


def get_ha_devices() -> list[dict]:
    token = _ha_token()
    if not token:
        return []
    try:
        req = urllib.request.Request(
            f"{HA_URL}/api/states",
            headers={"Authorization": f"Bearer {token}"},
        )
        states = json.loads(urllib.request.urlopen(req, timeout=5).read())
    except Exception:
        return []

    devices = []
    for entity in states:
        eid = entity.get("entity_id", "")
        domain = eid.split(".")[0] if "." in eid else ""
        if domain in ("light", "switch", "fan", "climate", "cover", "lock", "media_player"):
            devices.append(
                {
                    "id": eid,
                    "domain": domain,
                    "name": entity.get("attributes", {}).get("friendly_name", eid),
                    "state": entity.get("state", "unknown"),
                }
            )
    return devices


_DEVICE_STOPWORDS = {
    "can",
    "you",
    "please",
    "the",
    "a",
    "my",
    "turn",
    "switch",
    "off",
    "on",
    "toggle",
    "set",
    "is",
    "it",
    "to",
}


def control_device(text: str, device: str = "", operation: str = "") -> dict:
    """Deterministic device control — no LLM. When the router supplies the
    resolved device + operation, use them directly; otherwise parse the text."""
    devices = get_ha_devices()
    if not devices:
        return _envelope(
            "error",
            "No smart devices connected yet. Add them in the dashboard "
            "at http://minder.local (Devices tab).",
        )

    op = (operation or "").lower()
    if op in ("on", "off", "toggle"):
        action = {"on": "turn_on", "off": "turn_off", "toggle": "toggle"}[op]
    else:
        words = re.findall(r"[a-z0-9]+", text.lower())
        if "off" in words:
            action = "turn_off"
        elif "on" in words:
            action = "turn_on"
        elif "toggle" in words:
            action = "toggle"
        else:
            names = ", ".join(d["name"] for d in devices[:15])
            return _envelope("device_action", f"Tell me on or off. Devices: {names}")

    # Match against the router's device name if given, else the message words.
    query_src = device or text
    query = set(re.findall(r"[a-z0-9]+", query_src.lower())) - _DEVICE_STOPWORDS
    best, best_key = None, (0, 0)
    for d in devices:
        name_words = set(re.findall(r"[a-z0-9]+", d["name"].lower()))
        score = len(query & name_words)
        # Prefer available entities — HA keeps ghost duplicates around
        key = (score, 0 if d["state"] == "unavailable" else 1)
        if score > 0 and key > best_key:
            best, best_key = d, key

    if not best:
        names = ", ".join(d["name"] for d in devices[:15])
        return _envelope("device_action", f"Couldn't match that to a device. Available: {names}")

    token = _ha_token()
    try:
        req = urllib.request.Request(
            f"{HA_URL}/api/services/{best['domain']}/{action}",
            data=json.dumps({"entity_id": best["id"]}).encode(),
            headers={"Authorization": f"Bearer {token}", "Content-Type": "application/json"},
            method="POST",
        )
        urllib.request.urlopen(req, timeout=10)
        verb = {"turn_off": "turned off", "turn_on": "turned on"}.get(action, "toggled")
        return _envelope(
            "device_action",
            f"✅ {best['name']} - {verb}",
            data={"device": best["name"], "action": action, "ok": True},
        )
    except Exception as e:
        return _envelope(
            "device_action",
            f"❌ {best['name']} - failed: {e}",
            data={"device": best["name"], "action": action, "ok": False},
        )


def list_devices() -> dict:
    devices = get_ha_devices()
    if not devices:
        return _envelope(
            "device_list",
            "No smart devices connected yet. Add them at http://minder.local (Devices tab).",
        )
    lines = [f"{len(devices)} device(s):"]
    lines += [f"  • {d['name']} - {d['state']}" for d in devices]
    return _envelope("device_list", "\n".join(lines), data={"devices": devices})


