# The minder-scenario topology is a single worker agent whose output_schema
# carries the rule fields. Schema-constrained decoding (SwarmKit >= 1.3.5)
# makes even the 3B local model emit on-schema JSON, so the agent extracts and
# this module validates the field values and persists the rule — the agent
# never writes anything.


def _normalize_time(s: str) -> str:
    s = (s or "").strip().lower().replace(".", ":")
    m = re.fullmatch(r"(\d{1,2})(?::(\d{2}))?\s*(am|pm)?", s)
    if not m:
        return ""
    h, minute = int(m[1]), int(m[2] or 0)
    if m[3] == "pm" and h != 12:
        h += 12
    if m[3] == "am" and h == 12:
        h = 0
    if not (0 <= h < 24 and 0 <= minute < 60):
        return ""
    return f"{h:02d}:{minute:02d}"


def _match_camera_name(requested: str) -> str:
    requested = (requested or "").lower().strip()
    if requested in ("", "all", "any", "all cameras"):
        return "all"
    req_words = set(re.findall(r"[a-z0-9]+", requested))
    best, best_score = "all", 0
    for c in _load_cameras():
        name = c.get("name") or c["ip"]
        words = set(re.findall(r"[a-z0-9]+", name.lower()))
        score = len(req_words & words)
        if score > best_score:
            best, best_score = name, score
    return best


def _match_device_name(requested: str) -> str | None:
    req_words = set(re.findall(r"[a-z0-9]+", (requested or "").lower())) - _DEVICE_STOPWORDS
    best, best_key = None, (0, 0)
    for d in get_ha_devices():
        words = set(re.findall(r"[a-z0-9]+", d["name"].lower()))
        score = len(req_words & words)
        key = (score, 0 if d["state"] == "unavailable" else 1)
        if score > 0 and key > best_key:
            best, best_key = d["name"], key
    return best


async def create_scenario(text: str) -> dict:
    """Create a rule from natural language via the minder-scenario worker
    agent. The agent extracts on-schema fields (output_schema-constrained);
    this module validates the values and persists the rule. The reply is
    always formatted from the rule that was actually persisted."""
    clean = re.sub(r"^/scenario\s*", "", text.strip(), flags=re.I)

    context = build_context()
    enriched = f"{context}\n\n{clean}" if context else clean
    try:
        output = await asyncio.wait_for(run_topology("minder-scenario", enriched), timeout=120)
        parsed = _extract_json(output)
    except Exception as e:
        return _envelope("error", f"Scenario creation failed: {e}")
    if not parsed:
        return _envelope(
            "error",
            "I couldn't understand that scenario. Try: "
            '"when someone is outside after 10pm, turn on the porch light"',
        )

    return _persist_scenario(parsed, clean)


def _extract_json(output: str) -> dict | None:
    """Parse the worker's structured output. With output_schema the agent
    returns raw JSON, but tolerate stray text around it just in case."""
    output = (output or "").strip()
    if not output:
        return None
    try:
        obj = json.loads(output)
        return obj if isinstance(obj, dict) else None
    except (json.JSONDecodeError, ValueError):
        pass
    start, end = output.find("{"), output.rfind("}")
    if 0 <= start < end:
        try:
            obj = json.loads(output[start : end + 1])
            return obj if isinstance(obj, dict) else None
        except (json.JSONDecodeError, ValueError):
            return None
    return None


def _device_grounded_in_request(device: str, request: str) -> bool:
    """A required string field forces the small model to emit *a* device even
    for alert-only requests. Keep the device only if the user's words actually
    overlap the matched device name — otherwise it was hallucinated."""
    dev_words = set(re.findall(r"[a-z0-9]+", (device or "").lower())) - _DEVICE_STOPWORDS
    req_words = set(re.findall(r"[a-z0-9]+", (request or "").lower()))
    return bool(dev_words & req_words)


def _rule_cameras(rule: dict) -> list[str]:
    """Cameras a rule watches, as a list. Backward-compatible with rules that
    stored a single ``camera`` string before multi-camera support."""
    cams = rule.get("cameras")
    if isinstance(cams, list) and cams:
        return cams
    return [rule.get("camera", "all")]


def _coerce_str_list(value: object, single_key_value: object = None) -> list[str]:
    """Normalize a worker field to a list of non-empty strings, tolerating the
    old single-string field (``single_key_value``) and stray scalars."""
    if isinstance(value, list):
        items = value
    elif value is None:
        items = [single_key_value] if single_key_value else []
    else:
        items = [value]
    return [str(x).strip() for x in items if str(x).strip()]


def _schedule_to_ha_condition(schedule: str) -> list[dict]:
    """Map a rule schedule to an HA time condition (for motion reflexes). HA
    handles after>before as a window that wraps midnight."""
    s = (schedule or "always").strip()
    if s in ("", "always"):
        return []
    if s == "night":
        return [{"condition": "time", "after": "20:00:00", "before": "06:00:00"}]
    if s == "day":
        return [{"condition": "time", "after": "06:00:00", "before": "20:00:00"}]
    m = re.fullmatch(r"(\d{1,2}):(\d{2})-(\d{1,2}):(\d{2})", s)
    if m:
        return [
            {
                "condition": "time",
                "after": f"{int(m[1]):02d}:{m[2]}:00",
                "before": f"{int(m[3]):02d}:{m[4]}:00",
            }
        ]
    return []


def _camera_motion_entities(cameras: list[str]) -> list[str] | None:
    """Resolve a rule's cameras to their HA motion sensors — but only if EVERY
    camera is HA-tier. Returns None otherwise (Frigate detection isn't an HA
    entity, so HA can't trigger on it; those stay Minder-managed)."""
    if not cameras or "all" in cameras:
        return None
    inv = {(c.get("name") or "").lower(): c for c in _load_cameras()}
    ents = []
    for name in cameras:
        c = inv.get((name or "").lower())
        if not c or c.get("tier") != "ha-snapshot" or not c.get("ha_entity"):
            return None
        ents.append(c["ha_entity"])
    return ents or None


def _ha_device_services(device_actions: list[dict]) -> list[dict]:
    by_name = {d["name"].lower(): d for d in get_ha_devices()}
    out = []
    for act in device_actions:
        d = by_name.get((act.get("device") or "").lower())
        if d:
            out.append(
                {"service": f"{d['domain']}.{act['action']}", "target": {"entity_id": d["id"]}}
            )
    return out


def _deploy_automation(
    automation_id: str,
    triggers: list[dict],
    conditions: list[dict],
    device_actions: list[dict],
    label: str,
) -> str:
    """Deploy a native HA automation (trigger → device service calls) via HA's
    config API. Returns the automation id on success, '' otherwise."""
    token = _ha_token()
    if not token:
        return ""
    ha_actions = _ha_device_services(device_actions)
    if not ha_actions:
        return ""
    config = {
        "alias": f"Minder: {label}",
        "trigger": triggers,
        "condition": conditions,
        "action": ha_actions,
        "mode": "single",
    }
    try:
        for path, body in (
            (f"/api/config/automation/config/{automation_id}", config),
            ("/api/services/automation/reload", {}),
        ):
            req = urllib.request.Request(
                f"{HA_URL}{path}",
                data=json.dumps(body).encode(),
                headers={"Authorization": f"Bearer {token}", "Content-Type": "application/json"},
                method="POST",
            )
            urllib.request.urlopen(req, timeout=10)
        return automation_id
    except Exception:
        return ""


def _compile_ha_reflex(rule: dict) -> str:
    """If the scenario is a pure HA reflex — device-only (no alert) on either a
    daily time trigger OR motion from HA-tier cameras — deploy it as a native
    HA automation and return its id. Else '' (stays Minder-managed). Native HA
    automations are exact, instant, and survive Minder being down."""
    actions = rule.get("actions", [])
    device_actions = [a for a in actions if a.get("type") == "device"]
    if not device_actions or any(a.get("type") == "alert" for a in actions):
        return ""
    aid = f"minder_{int(rule['created_ts'])}"
    at_time, condition = rule.get("at_time"), rule.get("condition")
    if at_time and not condition:
        return _deploy_automation(
            aid,
            [{"platform": "time", "at": f"{at_time}:00"}],
            [],
            device_actions,
            f"every day at {at_time}",
        )
    if condition and not at_time:
        motion = _camera_motion_entities(rule.get("cameras", []))
        if motion:
            return _deploy_automation(
                aid,
                [{"platform": "state", "entity_id": motion, "to": "on"}],
                _schedule_to_ha_condition(rule.get("schedule", "always")),
                device_actions,
                f"{condition} → device",
            )
    return ""


def _plan_to_rule(plan: dict) -> tuple[dict, str]:
    """Map the router's scenario plan to the fields _persist_scenario expects.
    The router already parsed the trigger/device/schedule, so this is a pure
    translation — no LLM. Returns (parsed, trigger_type)."""
    tt = (plan.get("trigger_type") or "").lower()
    op = (plan.get("operation") or "").lower()
    device = (plan.get("device") or "").strip()
    parsed: dict = {
        "cameras": [],
        "condition": "",
        "schedule": (plan.get("schedule") or "always").strip() or "always",
        "at_time": "",
        "devices": [device] if device else [],
        "device_action": {"on": "turn_on", "off": "turn_off"}.get(op, ""),
        "alert": True,
    }
    if tt == "vision":
        obj = (plan.get("trigger_object") or "person").strip().lower()
        parsed["condition"] = f"is there a {obj}"
        parsed["cameras"] = [plan.get("trigger_camera") or "all"]
    elif tt == "time":
        parsed["at_time"] = plan.get("at") or ""
    return parsed, tt


async def author_scenario(plan: dict, text: str) -> dict:
    """Deterministic scenario authoring from the router's plan — no second agent.
    Sensor triggers aren't supported by the rule engine yet (vision + time only)."""
    parsed, tt = _plan_to_rule(plan)
    if tt == "sensor":
        return _envelope(
            "scenario",
            "Sensor-based rules (like 'when the water level is reached') aren't "
            "supported yet — I can trigger on what a camera sees or at a set time.",
        )
    return await asyncio.to_thread(_persist_scenario, parsed, text)


def _persist_scenario(parsed: dict, request: str = "") -> dict:
    """Validate the worker's extracted fields and write the rule. Supports
    multiple cameras and multiple devices, and compiles pure time→device
    reflexes to native HA automations (Phase 5)."""
    at_time = _normalize_time(parsed.get("at_time", ""))
    condition = (parsed.get("condition") or "").strip()
    if not condition and not at_time:
        return _envelope(
            "error",
            "I couldn't understand that scenario. Try: "
            '"when someone is outside after 10pm, turn on the porch light"',
        )

    # Cameras: match each requested name; "all" collapses the whole rule to all.
    requested_cams = _coerce_str_list(parsed.get("cameras"), parsed.get("camera"))
    if not requested_cams:
        requested_cams = ["all"]
    cameras: list[str] = []
    for c in requested_cams:
        m = _match_camera_name(c)
        if m == "all":
            cameras = ["all"]
            break
        if m not in cameras:
            cameras.append(m)
    if not cameras:
        cameras = ["all"]

    schedule = (parsed.get("schedule") or "always").strip()
    if not re.fullmatch(r"always|night|day|\d{1,2}:\d{2}-\d{1,2}:\d{2}", schedule):
        schedule = "always"

    # Devices: one action applied to every grounded, matched device.
    requested_devs = _coerce_str_list(parsed.get("devices"), parsed.get("device"))
    action = parsed.get("device_action")
    actions: list[dict] = []
    warnings: list[str] = []
    if action in ("turn_on", "turn_off"):
        seen: set[str] = set()
        for d in requested_devs:
            if not _device_grounded_in_request(d, request):
                continue
            matched = _match_device_name(d)
            if matched and matched not in seen:
                seen.add(matched)
                actions.append({"type": "device", "device": matched, "action": action})
            elif not matched:
                warnings.append(f"no device matched '{d}'")
    if parsed.get("alert", True) or not actions:
        actions.insert(0, {"type": "alert"})

    created_ts = time.time()
    rule = {
        "cameras": cameras,
        "condition": condition,
        "schedule": schedule,
        "enabled": True,
        "actions": actions,
        "at_time": at_time,
        "created_ts": created_ts,
        "target": "minder",
    }

    # Phase 5 — compile pure device reflexes (time→device, or motion→device on
    # HA-tier cameras) to a native HA automation. Tagged target="ha" so the
    # Minder poller skips it (no double-fire); it still lives in rules.json so
    # the rule list stays complete.
    aid = _compile_ha_reflex(rule)
    if aid:
        rule["target"] = "ha"
        rule["ha_automation_id"] = aid

    rules = []
    if RULES_FILE.exists():
        with contextlib.suppress(json.JSONDecodeError, ValueError):
            rules = json.loads(RULES_FILE.read_text())
    for existing in rules:
        if (
            sorted(_rule_cameras(existing)) == sorted(cameras)
            and existing.get("condition") == rule["condition"]
            and existing.get("schedule") == rule["schedule"]
            and existing.get("at_time", "") == rule["at_time"]
        ):
            return _envelope(
                "scenario", "That scenario already exists.", data={"rules": [existing]}
            )
    rules.append(rule)
    write_json_atomic(RULES_FILE, rules)

    created = [rule]
    if warnings:
        created[0]["_warnings"] = warnings
    return _format_scenario_reply(created)


def _format_scenario_reply(created: list[dict]) -> dict:
    parts = []
    for rule in created:
        cams = _rule_cameras(rule)
        cam_label = "all cameras" if cams == ["all"] else ", ".join(cams)
        if rule.get("at_time"):
            where = " (runs locally in Home Assistant)" if rule.get("target") == "ha" else ""
            parts.append(f"Scenario created - every day at {rule['at_time']}{where}:")
        else:
            parts.append(
                f'Scenario created - when "{rule["condition"]}" on '
                f"{cam_label} ({rule.get('schedule', 'always')}):"
            )
        for a in rule.get("actions", []):
            if a["type"] == "alert":
                parts.append("  • send an alert")
            else:
                parts.append(f"  • {a['action'].replace('_', ' ')} {a['device']}")
        for w in rule.get("_warnings", []):
            parts.append(f"  ⚠ {w}")
    return _envelope("scenario", "\n".join(parts), data={"rules": created})


# ---- Recovery (backup / restore / doctor / health) ----
#
# State is spread across volumes; writes are atomic (prevent corruption) and a
# small set of PRECIOUS files are backed up so they can be restored. Everything
# else is derived (Frigate config ← cameras.json) or transient, so the doctor
# regenerates rather than restores it.


def _file_status(path: Path) -> str:
    if not path.exists():
        return "missing"
    try:
        data = json.loads(path.read_text())
    except (json.JSONDecodeError, ValueError):
        return "corrupt"
    return "empty" if data in ([], {}, None) else "ok"


def backup() -> dict:
    """Snapshot the parseable precious files to a timestamped backup dir and
    rotate. Never backs up a corrupt file (would poison a restore)."""
    ts = time.strftime("%Y%m%d-%H%M%S", time.localtime())
    dest = BACKUP_DIR / ts
    dest.mkdir(parents=True, exist_ok=True)
    saved = []
    for name in PRECIOUS_FILES:
        src = DATA_DIR / name
        if src.exists() and _file_status(src) in ("ok", "empty"):
            (dest / name).write_text(src.read_text())
            saved.append(name)
    dirs = sorted([p for p in BACKUP_DIR.iterdir() if p.is_dir()])
    for old in dirs[:-BACKUP_KEEP]:
        shutil.rmtree(old, ignore_errors=True)
    return {"ts": ts, "files": saved, "path": str(dest)}


def list_backups() -> list[dict]:
    if not BACKUP_DIR.exists():
        return []
    return [
        {"ts": p.name, "files": sorted(f.name for f in p.iterdir())}
        for p in sorted((d for d in BACKUP_DIR.iterdir() if d.is_dir()), reverse=True)
    ]


def _ha_tar_filter(tarinfo):
    """Skip the recorder DB, logs, and TTS cache — huge and disposable. Keep
    .storage (auth/entities/integrations), automations, custom_components."""
    base = tarinfo.name.rsplit("/", 1)[-1]
    if (
        base.startswith("home-assistant_v2.db")
        or base.endswith((".log", ".log.1"))
        or "/tts/" in tarinfo.name
        or "/deps/" in tarinfo.name
    ):
        return None
    return tarinfo


def backup_ha_volume() -> dict:
    """Tar the HA config volume (config-only, no recorder DB) to the host
    backups path for full-stack disaster recovery. Live snapshot — HA writes
    .storage atomically per-file, so this is consistent enough for DR."""
    import tarfile

    if not HA_VOLUME_DIR.exists():
        return {"status": "skipped", "reason": "ha-config not mounted"}
    BACKUP_DIR.mkdir(parents=True, exist_ok=True)
    ts = time.strftime("%Y%m%d-%H%M%S", time.localtime())
    out = BACKUP_DIR / f"ha-config-{ts}.tar.gz"
    tmp = out.with_suffix(".tmp")
    try:
        with tarfile.open(tmp, "w:gz") as tf:
            tf.add(HA_VOLUME_DIR, arcname=".", filter=_ha_tar_filter)
        os.replace(tmp, out)
    except Exception as e:
        return {"status": "error", "message": str(e)[:200]}
    for old in sorted(BACKUP_DIR.glob("ha-config-*.tar.gz"))[:-BACKUP_KEEP]:
        old.unlink(missing_ok=True)
    return {"status": "ok", "file": out.name, "size_kb": out.stat().st_size // 1024}


def _latest_good_backup(filename: str) -> Path | None:
    if not BACKUP_DIR.exists():
        return None
    for d in sorted((d for d in BACKUP_DIR.iterdir() if d.is_dir()), reverse=True):
        cand = d / filename
        if cand.exists() and _file_status(cand) in ("ok", "empty"):
            return cand
    return None


def _is_problem(name: str, status: str) -> bool:
    """A precious file is a genuine fault only if it's corrupt (restore from
    backup), or missing AND a good backup exists (it was there before → real
    loss). Missing with no backup is just not-set-up-yet (fresh install / no
    scenarios) — not a fault, so it never raises an alert."""
    if status == "corrupt":
        return True
    if status == "missing":
        return _latest_good_backup(name) is not None
    return False


def restore(ts: str = "") -> dict:
    """Restore precious files from a backup (a specific ts, or the latest good
    copy of each file if ts is empty)."""
    restored = []
    for name in PRECIOUS_FILES:
        src = (BACKUP_DIR / ts / name) if ts else _latest_good_backup(name)
        if src and src.exists() and _file_status(src) in ("ok", "empty"):
            write_json_atomic(DATA_DIR / name, json.loads(src.read_text()))
            restored.append(name)
    return {"restored": restored, "ts": ts or "latest"}


def health() -> dict:
    files = {name: _file_status(DATA_DIR / name) for name in PRECIOUS_FILES}
    frigate_ok = False
    try:
        urllib.request.urlopen(f"{FRIGATE_URL}/api/version", timeout=5)
        frigate_ok = True
    except Exception:
        pass
    backups = list_backups()
    ha_tars = sorted(BACKUP_DIR.glob("ha-config-*.tar.gz")) if BACKUP_DIR.exists() else []
    return {
        "files": files,
        "ha": bool(_ha_token()),
        "frigate": frigate_ok,
        "backups": len(backups),
        "latest_backup": backups[0]["ts"] if backups else None,
        "ha_volume_backups": len(ha_tars),
        "latest_ha_volume_backup": ha_tars[-1].name if ha_tars else None,
        # Healthy unless a file is a genuine fault. Missing-on-fresh-install
        # (no backup yet) is "not set up", not unhealthy.
        "healthy": not any(_is_problem(n, s) for n, s in files.items()),
    }


def _review_queue() -> FileReviewQueue:
    return FileReviewQueue(DATA_DIR)


def _pending_repairs() -> list:
    return [it for it in _review_queue().list_pending() if it.skill_id == _REPAIR_MARKER]


def diagnose_and_alert() -> dict:
    """Detect problems and, if unhealthy, file a repair as a SwarmKit review
    item (the native HITL gate) and alert the human to approve it. NEVER fixes
    anything — the fix runs only when the review item is resolved 'approved'."""
    h = health()
    # Only file a repair the doctor can actually apply (restore corrupt/lost
    # files). HA/Frigate being unreachable isn't doctor-repairable — it's shown
    # in health() but never raises a repair-approval alert.
    bad = [f"{f} ({s})" for f, s in h["files"].items() if _is_problem(f, s)]
    if bad:
        if not _pending_repairs():
            item = create_review_item(
                topology_id="minder-doctor",
                agent_id="doctor",
                skill_id=_REPAIR_MARKER,
                output="Restore corrupt/missing state from the last good backup "
                "and regenerate derived config.",
                verdict="needs-revision",
                reason="Health issue: " + "; ".join(bad),
            )
            _review_queue().submit(item)
            short = item.id[:8]
            _sink_write_alert(
                "Minder found a problem and needs your approval to fix it: "
                + "; ".join(bad)
                + f".\nApprove: /approve {short}   Dismiss: /reject {short}",
                "",
            )
            h["review_id"] = item.id
        else:
            pending = _pending_repairs()[0]
            h["review_id"] = pending.id
            # Re-remind while still pending (the 6h doctor cron drives this);
            # 5h cooldown so restart storms don't spam.
            st = read_json_safe(DATA_DIR / "monitor_state.json", {}) or {}
            if time.time() - st.get("_health_reminder_ts", 0) > 5 * 3600:
                _sink_write_alert(
                    "Reminder: a repair still needs your approval — "
                    f"/approve {pending.id[:8]} (or open the dashboard).",
                    "",
                )
                st["_health_reminder_ts"] = time.time()
                write_json_atomic(DATA_DIR / "monitor_state.json", st)
    return h


def list_approvals() -> list[dict]:
    """Pending repair approvals (SwarmKit review items awaiting a human)."""
    return [
        {
            "id": it.id,
            "short": it.id[:8],
            "reason": it.reason,
            "action": it.output,
            "ts": it.timestamp,
        }
        for it in _pending_repairs()
    ]


async def approve_repair(item_id: str = "") -> dict:
    """Human approval: resolve the review item 'approved' and THEN run the
    repair. This is the only path that applies a fix."""
    pending = _pending_repairs()
    if not pending:
        return {"status": "none", "message": "No repair is awaiting approval."}
    item = (
        next((it for it in pending if it.id.startswith(item_id)), pending[0])
        if item_id
        else pending[0]
    )
    _review_queue().resolve(item.id, "approved")
    report = await doctor(repair=True)  # gated action — runs only after approval
    return {
        "status": "approved",
        "item_id": item.id,
        "repaired": report.get("repaired", []),
        "regenerated": report.get("regenerated", []),
    }


def reject_repair(item_id: str = "") -> dict:
    """Human dismissal: resolve the review item 'rejected'; no fix runs."""
    pending = _pending_repairs()
    if not pending:
        return {"status": "none"}
    item = (
        next((it for it in pending if it.id.startswith(item_id)), pending[0])
        if item_id
        else pending[0]
    )
    _review_queue().resolve(item.id, "rejected")
    return {"status": "rejected", "item_id": item.id}


async def doctor(repair: bool = False) -> dict:
    """Validate state; when repair=True, restore corrupt/missing precious files
    from the latest good backup and regenerate derived state (Frigate config
    from cameras.json)."""
    report: dict = {"checked": {}, "repaired": [], "regenerated": []}
    for name in PRECIOUS_FILES:
        st = _file_status(DATA_DIR / name)
        report["checked"][name] = st
        if repair and st in ("missing", "corrupt"):
            good = _latest_good_backup(name)
            if good:
                write_json_atomic(DATA_DIR / name, json.loads(good.read_text()))
                report["repaired"].append(name)
    if repair and _file_status(DATA_DIR / "cameras.json") == "ok":
        try:
            await run_topology("minder-frigate-config", "Configure Frigate", timeout=120)
            report["regenerated"].append("frigate-config")
        except Exception:
            pass
    return report


